// 아! 그러면 아톰의 값이 바뀐다는 의미는 지금 내가 하고싶은 것이 사용자 정보에 id를 부여하고 싶은데 onSet에 id를 부여하는 로직을 넣게된다면 사용자의 정보가 들어왔는데 id값을 다시 부여해야하니 onSet 내부에 id를 달아주는 로직이 발동하는데 결국 onSet이 값이 변경되었다고 인식하고 그걸 뱉으면 또 아톰이 값이 변경되었다고 인식해서 다시 onSet이 발동한다!
