//onSet은 아틈의 값이 변경되었을 때 실행되는 리스너이다
//컴포넌트에서 setUserInfo를 호출을 한다
//값이 바뀌었으므로 effects 내부 onSet 함수가 실행이된다
//onSet함수가 실행이되어 값이 바뀌면 아톰의 값이 또 바뀐다
//아톰의 값이 바뀌었으니 onSet이 다시 실행이 된다 결국 이렇게 무한반복이 되면서 뻗어버린다
