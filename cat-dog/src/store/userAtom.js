import { atom } from "recoil";

const realLocalStorageEffect =
  (key) =>
  ({ setSelf, onSet }) => {
    const savedValue = localStorage.getItem(key);

    if (savedValue != null) {
      setSelf(JSON.parse(savedValue));
    }

    //   Effects에는 2개의 속성이 있는것처럼 보인다
    //setSelf : useState처럼 만약에 이전에 값이 있다면 그 값에서 가져온다
    //onSet은 useState에 값이 들어올 때 그 값을 저장한다
    //isReset은 뭔지 모르겠다 나중에 찾아봐야지 근데 뭔가 저 기능이 돌아가면 기존에 있는 값들이 다 지워지는거 같다
    //isReset 기능을 찾았다 로그아웃 기능이었다 저 기능을 사용하면 로컬스토리지안에 있는 정보들을 삭제하면서 로그아웃이 가능해진다

    onSet((newValue, _, isReset) => {
      if (isReset) {
        localStorage.removeItem(key);
      } else {
        localStorage.setItem(key, JSON.stringify(newValue));
      }
    });
  };

//id를 식별할 수 있는 코드를 짜서 넣어줘야한다
